mood = input("What is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times and think about your happy place.")
elif mood == "sad":
    print("Cheer up mate!")
elif mood == "excited":
    print("Yay! Me too!")
elif mood == "relaxed":
    print("Let's drink a warm cup of tea together.")
else:
    print("Can you say that again?")